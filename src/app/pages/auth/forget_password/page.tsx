import { redirect } from "next/navigation";
import MainContent from "./MainContent";
import { auth } from "@/src/auth";


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


export default async function page() {
    
    const session = await auth()
    if (session?.user) {
        redirect("/")
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    return (
        <>
            <>
                <MainContent />
            </>
        </>
    );
}
